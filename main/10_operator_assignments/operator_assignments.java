public class operator_assignments{
    public static void main(String[] args) {
        
        System.out.println("Operator assignments");
        int x = 1;
        System.out.println("Nilai awal x : " + x);
        x += 5; // sama aja x = x + 5;
        System.out.println("Nilai x += 5 : " + x); // berlaku untuk semua
        System.out.println("Dah itu aja ^_^");

    }
}