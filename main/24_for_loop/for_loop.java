import java.util.Scanner;

public class for_loop {
    public static void main(String[] args) {
        System.out.println("For loop");
        System.out.println("\nBagian 1\n");

        // cara 1
        for(int i = 1; i <= 10; i++){
            System.out.println("Pengulangan ke : " + i);
        }


        // cara 2
        System.out.println("\nBagian 2\n");
        int Total = 1;
        for (; Total <= 10 ;){
            System.out.println("Pengulangan ke : " + Total);
            Total++;
        }    
        
        System.out.println("\nAkhir Program");

        // dan masih banyak lainnya maikan aja logika lu ya
        // gw cukup beri dasar aja ^_^


    }
}